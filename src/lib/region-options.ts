/**
 * region 문자열을 "{1단계} {2단계}" 로 합성. 2단계가 비어있으면 1단계만.
 */
export function formatRegion(r1: Region1 | "", r2: string): string {
  if (!r1) return "";
  if (!r2) return r1;
  return `${r1} ${r2}`;
}

/**
 * 저장된 region 문자열을 역파싱. 알려진 1단계 prefix 가 없으면
 * 1단계 빈 값 + 원본 보존 (기존 자유 입력 호환).
 */
export function parseRegion(
  value: string | null | undefined
): { r1: Region1 | ""; r2: string } {
  if (!value) return { r1: "", r2: "" };
  const trimmed = value.trim();
  for (const r1 of REGION1_OPTIONS) {
    if (trimmed === r1) return { r1, r2: "" };
    if (trimmed.startsWith(r1 + " ")) {
      return { r1, r2: trimmed.slice(r1.length + 1).trim() };
    }
  }
  return { r1: "", r2: trimmed };
}

export function isMetro(r1: Region1 | ""): boolean {
  return !!r1 && METRO_CITIES.includes(r1 as Region1);
}
